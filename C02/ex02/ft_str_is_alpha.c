#include <stdio.h>

int	ft_str_is_alpha(char *str)
{
	int		i;
	char	b;

	i = 0;
	while (true)
	{	
		b = str[i];
		if (b == '\0')
		{
			return (1);
		}
		if (!((b >= 'A' && b <= 'Z') || (b >= 'a' && b <= 'z')))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
